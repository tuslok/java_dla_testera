package FirstModule;

public class Demo {

    public static void main(String[] args) {
        System.out.println("FirstModule.Demo");

        // liczby całkowite
        int number; //deklaracja zmiennej
        number = 18; //inicjalizacja zmiennej

        int number10 = 10;
        System.out.print(number10);

        // liczby zmiennoprzecinkowe
        double doubleNumber = 5.6;
        System.out.print(doubleNumber);

        // tekst
        String someText = "text in Java"; // ciąg znaków w podwójny cudzysłów
        System.out.print(someText);

        //znak
        char someChar = 't'; // jeden znak w pojedyńczym cudzysłowie
        System.out.print(someChar);

        //prawda
        boolean trueValue = true;
        System.out.print(trueValue);

        //fałsz
        boolean falseValue = false;
        System.out.print(falseValue);
    }
}
